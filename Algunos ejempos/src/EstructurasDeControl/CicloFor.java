public class CicloFor {
    public static void main(String[] args) {
        //instruccion for, dentro de las llaves se ejecuta repetitivamente
        //(int i - o;variable de control)
        //(i <= 0 se ejecuta siempre y cuando la i sea = o menor al 0)
        //(i++ en cuanto avanza la condicion)
        for (int i = 0; i <= 0; i++) {
        System.out.println(i);
        }
        System.out.println("Fin del ciclo");
    }
}
